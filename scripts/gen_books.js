let isbnCounter = 0;
function genIsbn(catId) { isbnCounter++; const raw='9787'+String(catId).padStart(2,'0')+String(isbnCounter).padStart(5,'0'); let s=0; for(let i=0;i<raw.length;i++) s+=parseInt(raw[i])*(i%2===0?1:3); const c=(10-(s%10))%10; return `978-7-${String(catId).padStart(2,'0')}-${String(isbnCounter).padStart(5,'0')}-${c}`; }
function esc(s) { if(!s) return ''; return s.replace(/\\/g,'\\\\').replace(/'/g,"\\'"); }
function rand(a,b) { return Math.floor(Math.random()*(b-a+1))+a; }

